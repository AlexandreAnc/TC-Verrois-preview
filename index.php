        document.addEventListener('DOMContentLoaded', function() {
            // Vérification de la disponibilité de ScrollMagic et GSAP
            if (typeof ScrollMagic === 'undefined' || typeof gsap === 'undefined') {
                console.error('ScrollMagic or GSAP is not loaded');
                return;
            }

            // Initialisation du contrôleur ScrollMagic
            var controller = new ScrollMagic.Controller();

            // Première scène pour l'animation de la barre séparatrice
            new ScrollMagic.Scene({
                triggerElement: '.separator', // Élément déclencheur de l'animation
                duration: window.innerHeight, // Durée basée sur la hauteur de la fenêtre
                triggerHook: 0.5, // Déclenchement au milieu de l'écran
            })
            .on('progress', function(e) {
                // Ajustement de l'animation GSAP en fonction de la progression du défilement
                gsap.to('.separator .bar', {
                    width: e.progress * 100 + '%' // Liaison de la largeur de la barre à la progression
                });
            })
            .addTo(controller); // Ajoute la scène au contrôleur
        });
