                <br>
                <ul>
                    <li>RSE : mise en place d’une charte environnement, <strong> au covoiturage et au recyclage des balles</strong></li>
                    <br>
                    <li>Autonomie financière : <strong>recherche de partenaires nouveaux et renouvellement des anciens</strong>, avec la mise en place d’un accord avec Larde Sports / Tennispro Angers, augmentation significative des tarifs d’inscription pour la troisième saison consécutive</li>
                    <br>
                    <li>Le développement d’activités complémentaires aux cours afin de renforcer les liens sociaux entre les licenciés notamment au niveau intergénérationnel avec la mise en place d’un tournoi des familles en complément de la traditionnelle fête du club</li>
                    <br>
                    <li>Développement d’une pratique sportive pour tous (loisirs, compétiteurs, seniors, jeunes) avec le maintien des inscriptions dans différents championnats, la mise en place d’un challenge hybride e-sport / tennis en salle en plus des traditionnels tournois Open et interne</li>
                </ul>
                <br>
                Pour la saison 2023/2024, le club poursuit les mêmes engagements et a notamment réduit ses horaires de cours de deux de ses soirées de cours pour laisser davantage de place à une pratique loisirs à la suite de la demande de quelques habitants de la commune relayée par la mairie.
                Dans le domaine des économies d’énergie, le correspondant du club sur le sujet coordonnera les initiatives en collaboration avec les équipes communales afin d’amplifier les mesures déjà prises.
                En complément, le club a organisé et financé des séances de découverte du tennis pour 17 classes des trois écoles primaires de la commune en mai et juin 2023
